    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        
        
        if (txtemployeeid.getText().equals("")||txtfirstname1.getText().equals("")||txtlastname.getText().equals("")||txtdepartment1.getSelectedItem().toString().equals("")||txtdesignation.getSelectedItem().toString().equals("")||txtgender.getSelectedItem().toString().equals("")||txtstatus1.getSelectedItem().toString().equals("")||txtcontactno.getText().equals("")){

           
            
            JOptionPane.showMessageDialog(this,"Please Enter All Data... !");
        }else{
            String data[] = {txtemployeeid.getText(),txtfirstname1.getText(),txtlastname.getText(),txtdepartment1.getSelectedItem().toString(),txtdesignation.getSelectedItem().toString(),txtgender.getSelectedItem().toString(),txtstatus1.getSelectedItem().toString(),txtcontactno.getText()};

            DefaultTableModel tblModel = (DefaultTableModel)tblema.getModel();

            tblModel.addRow(data);

            JOptionPane.showMessageDialog(this, "Add Data Successfully...1");

            txtemployeeid.setText("");
            txtfirstname1.setText("");
            txtlastname.setText("");
            txtdepartment1.setSelectedItem("");
            txtdesignation.setSelectedItem("");
            txtgender.setSelectedItem("");
            txtstatus1.setSelectedItem("");
            txtcontactno.setText("");
        }
        
        String filePath = "C:\\EMSS\\EMS.txt";
        File file = new File(filePath);
                
        try {
            
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            
            for(int i=0; i < tblema.getRowCount(); i++ ){
                for (int j=0; j < tblema.getColumnCount(); j++){
                    bw.write(tblema.getValueAt(i,j).toString()+" ");
                        
                }
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            
        } catch (IOException ex) {
            Logger.getLogger(EmployeeUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        

    }//GEN-LAST:event_btnAddActionPerformed

