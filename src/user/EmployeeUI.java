    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel tblModel =(DefaultTableModel) tblema.getModel();
        if(tblema.getSelectedRowCount()== 1){
            
            String empolyeeid = txtemployeeid.getText();
            String fristname = txtfirstname1.getText();
            String lastname = txtlastname.getText();
            String department =(String) txtdepartment1.getSelectedItem();
            String designation =(String) txtdesignation.getSelectedItem();
            String gender = (String) txtgender.getSelectedItem();
            String status = (String) txtstatus1.getSelectedItem();
            String contactno =  txtcontactno.getText();
            
            
            tblModel.setValueAt(empolyeeid,tblema.getSelectedRow(),0);
            tblModel.setValueAt(fristname,tblema.getSelectedRow(),1);
            tblModel.setValueAt(lastname,tblema.getSelectedRow(),2);
            tblModel.setValueAt(department,tblema.getSelectedRow(),3);
            tblModel.setValueAt(designation,tblema.getSelectedRow(),4);
            tblModel.setValueAt(gender,tblema.getSelectedRow(),5);
            tblModel.setValueAt(status ,tblema.getSelectedRow(),6);
            tblModel.setValueAt(contactno,tblema.getSelectedRow(),7);
            
            JOptionPane.showMessageDialog(this,"Update Successfully.");
            
        }else{
            if(tblema.getRowCount()==0){
        
        
                JOptionPane.showMessageDialog(this,"Table is empty");
            
            }else{
                    
                JOptionPane.showMessageDialog(this,"Please select Single Row");
                    
            }
       
        }

        String filePath = "C:\\EMSS\\EMS.txt";
        File file = new File(filePath);
                
        try {
            
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
            
            for(int i=0; i < tblema.getRowCount(); i++ ){
                for (int j=0; j < tblema.getColumnCount(); j++){
                    bw.write(tblema.getValueAt(i,j).toString()+" ");
                        
                }
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            
        } catch (IOException ex) {
            Logger.getLogger(EmployeeUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

